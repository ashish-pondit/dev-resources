
public class LiteralsJava {

	public static void main(String[] args) {
		byte data_decimal=15;
		byte data_binary=0b1111;
		byte data_octal=017;
		byte data_hexa=0XF;
		System.out.println("Same data in different number system 15");
		System.out.println("Decimal "+data_decimal);
		System.out.println("Binary "+data_binary);
		System.out.println("Octal "+data_octal);
		System.out.println("Hexadecimal "+data_hexa);

	}

}
