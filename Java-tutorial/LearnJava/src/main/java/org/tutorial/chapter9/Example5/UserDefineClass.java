package org.tutorial.chapter9.Example5;

public class UserDefineClass {
    private int value;

    public UserDefineClass(int value) {
        this.value = value;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return String.valueOf(value);
    }
}
